// 名前空間

// パッケージを使用する理由としてクラスのグループ化だけでなく、
// 自分が作るクラスに自由に名前をつけることが可能になる点もある。

// 異なるクラスで同じクラス名を使うと区別できないため原則許されず、
// 別々のクラスで同じ名前を取り合う　名前の衝突　が起きるがパッケージを使うことで
// それぞれのパッケージ内では自由にクラス名をつけることができる。

// パッケージ名の衝突を避ける方法
// 自社が保有するインターネットドメインを前後逆順にしたものから始まるパッケージ名が推奨されている。
// 例）　more-clean.workがドメインなら　パッケージ名は　work.more-clean
