// Java APIについて

// API（Application Programming Interface)とはJavaに初めから標準添付されているパッケージやクラスのこと。
// JavaではAPIとして200以上のパッケージ、3500以上のクラスが
// 標準提供されており我々はそれを自由に使用できる。
// 例）並び替えの命令
java.util.Arrays.sort();
// APIであるJava.utilパッケージのArrayクラスのsortメソッドを使用している。


// APIで提供される代表的なパッケージ
//  ・java.lang   Javaに欠かせない重要なクラス群
//     よく使用するためimport文が不要
//  ・java.util　　プログラミングを便利にする様々なクラス群
//  ・java.math　　数学に関するクラス群
//　・java.net　　　ネットワーク通信などを行うためのクラス群
//  ・java.io　　　ファイル読み書きなど、データを逐次処理するためのクラス群

// APIについて調べるにはAPIリファレンスと呼ばれる説明書を読めばよい。
